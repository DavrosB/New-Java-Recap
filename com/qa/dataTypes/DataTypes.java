package com.qa.dataTypes;

public class DataTypes {
	

	public static int intReturn(int num) {
	
		return num;
	}
	
	public static String stringReturn(String name) {
		
		return name;
	}
	
	public static boolean boolReturn(boolean val) {
		
		return val;
	}
	
	public static float floatReturn(float dec) {
		
		return dec;
	}
	
	
	public static char charReturn(char letter) {
		
		return letter;
	}

	public static long longReturn(long longNum) {
		
		return longNum;
		
	}

	public static short shortReturn(short shortNum) {
		
		return shortNum;
		
	}
	
}
   

   
